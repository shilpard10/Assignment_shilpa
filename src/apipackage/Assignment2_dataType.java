package apipackage;

public class Assignment2_dataType {

	public static void main(String[] args) {
		char gender = 'f';
		short number=1;
		int age=30;
		long monthlysalary=1000000000;
		byte b=1;
		float pi=3.14f;
		double salaryperyear=90000000000.50;
		boolean info=true;
		
		System.out.println("My gender is "+gender);
		System.out.println("My number is "+number);
		System.out.println("My age is "+age);
		System.out.println("My monthlysalary is "+monthlysalary);
		System.out.println("Byte value is "+b);
		System.out.println("Pi value is "+pi);
		System.out.println("My per year salary is "+salaryperyear);
		System.out.println("The above information is "+info);
		

	}

}
